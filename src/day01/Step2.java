package day01; //현재 클래스 파일이 위치한 패키지 폴더 명

public class Step2 {//class start  //모든 코드는 class 안에서만 작성한다.

    //main+엔터 => main 함수 // 코드 읽어주는 역할(스레드) 포함
    public static void main(String[] args) {  //main start

        int level; // 정수(소수점없는 숫자)형 변수 'level' 이름으로 선언
        level = 10;
        System.out.println(level);


        int level2 = 10; //정수형 'level2' 이름으로 변수 선언하면서 10대입
        System.out.println(level2);

    } //main end


    //출력하는 함수는 main 함수 안에서만 가능


}//class end


//클래스 밖에는 무조건 빨간색